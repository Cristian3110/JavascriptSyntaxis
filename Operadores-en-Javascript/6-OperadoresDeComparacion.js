
/****************************
 * OPeradores de comparación
 *****************************/
let a = 3, b = 2, c = "3";

let z = a == b; // se revisa el valor sin importar el tipo

console.log(z);

z = a === c; // revisa el valor y también los tipos
console.log(z);

//--------------------------------------------------

// Operador Distinto A en Javascript

z = a != b;
console.log(z);

z = a != c; // comparando valores diferentes
console.log(z);

z = a !== c; // comparando valores estrictamente diferentes
console.log(z);



